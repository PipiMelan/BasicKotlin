

fun main() {
    // The output sentence
    println("") //  There will be a line feed
    print("");

    //  Output Variable 输出变量
    val name = "rose"
    println(name)

    //  String Template
    println("My name is $name")

    //There will be a combination like this when the output variables are consist of multiple parts
    println("My name is $name and length is ${name.length}")
}